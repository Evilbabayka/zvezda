  jQuery(function($) {
    // Initialize page messages
    SIPageMessages.init();
    
    // Initialize phone input mask
    $('input[name="phone"]').mask('+7 (999) 999-99-99', {
      placeholder: "_",
      clearIncomplete: true,
      onComplete: function() {
        $(this).removeClass('si-error');
      },
      onIncomplete: function() {
        $(this).addClass('si-error');
      }
    }).on('focus', function() {
      // Set cursor position after +7
      const phone = $(this);
      const value = phone.val();
      if (value.indexOf('+7') === 0) {
        phone.setCursorPosition(3);
      }
    }).on('blur', function() {
      // Validate phone number on blur
      const phone = $(this);
      const phoneDigits = phone.val().replace(/\D/g, '');
      if (phoneDigits.length < 11) {
        phone.addClass('si-error');
      }
    });
    
    // Handle form submission
    $('.send-form').on('submit', function(e) {
      e.preventDefault();
      
      // Get form data
      const form = $(this);
      const phone = form.find('input[name="phone"]');
      const phoneValue = phone.val();
      const question = form.find('textarea[name="question"]').val();
      const group1 = form.find('input[name="group1"]:checked').val();
      const group2 = form.find('input[name="group2"]:checked').val();
      const group3 = form.find('input[name="group3"]:checked').val();
      
      // Validate phone number
      if (!phoneValue) {
        SIPageMessages.show('Пожалуйста, введите номер телефона');
        phone.addClass('si-error');
        return false;
      }
      
      // Validate phone number format
      const phoneDigits = phoneValue.replace(/\D/g, '');
      if (phoneDigits.length !== 11) {
        SIPageMessages.show('Пожалуйста, введите корректный номер телефона');
        phone.addClass('si-error');
        return false;
      }
      
      // Show loading state only on submit button
      const submitButton = form.find('.red-button-modal:not(.form-success-message .red-button-modal)');
      submitButton.prop('disabled', true).addClass('send-form__button--loading');
      
      // Prepare data for API
      const data = {
        question: `
          С чем связан вопрос: ${group1 || 'Не указано'}
          Ситуация сейчас: ${group2 || 'Не указано'}
          Что я хочу сделать: ${group3 || 'Не указано'}
          Текст вопроса: ${question || 'Не указано'}
          ${(group1 && group1.indexOf("(1)") == "-1" && 
             group2 && group2.indexOf("(1)") == "-1" && 
             group3 && group3.indexOf("(1)") == "-1") ? "(0)" : ""}
        `,
        phone: phoneValue,
        city: "-",
        referrer: window.location.href,
        key: "rU?a8ab$1ZHNMuqNcEJX"
      };
      
      // Send data to API
      $.ajax({
        url: 'https://service.apisuz.ru/api/push_bid',
        type: 'POST',
        data: JSON.stringify(data),
        contentType: 'application/json',
        success: function(response) {
          console.log('Form submission success:', response);
          
          // Hide form inputs and submit button
          form.find('input, textarea').hide();
          form.find('.btn-holder:not(.form-success-message .btn-holder)').hide();
          
          // Show success message with phone number
          form.find('.success-phone').text(phoneValue);
          form.find('.form-success-message').fadeIn();
          
          // Clear only phone field
          form.find('input[name="phone"]').val('');
          
          // Track goal
          if(typeof ym !== 'undefined') {
            ym(1111111111,'reachGoal','zayavka');
          }
        },
        error: function(xhr, status, error) {
          console.error('Form submission error:', error);
          console.error('Status:', status);
